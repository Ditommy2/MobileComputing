local composer = require( "composer" )
local lunghezza =  display.contentWidth
local altezza=  lunghezza*(9/16)

----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--Variabili nemico
----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
local enemy
local scene

----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--Creation enemy
----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
local function create(scena)
  --Memorizing scene context
  scene = scena

  --Displaying enemy
  enemy = display.newImageRect( "Images/Characters/joker.png", 200, 200)
  enemy.x = lunghezza * 0.7
  enemy.y = altezza-370
  enemy.myName = "Enemy"
  enemy.life = 600
  enemy.armor = 1
  enemy.damage = 80
  enemy.speed = 15

  return enemy
end

----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--Interface enemy
----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
local interfaceEnemy =
{
  createEnemy = (create),
}

return interfaceEnemy
